package edu.uv.dawts.trabajofinal;

//JavaBean Proyecto
public class Proyecto {
	private int id;
	private String nombre;

	public Proyecto() {
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
